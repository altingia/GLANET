package mappingsnps.keggpathways;

import java.util.HashMap;
import java.util.List;


public class KeggPathway {

//	example entry hsa04010  Pathway
	String entry;
//	example id hsa04010 
	String id;
//	example  name MAPK signaling pathway - Homo sapiens (human)
	String name;
//	example pathwayClass Environmental Information Processing; Signal transduction
	String pathwayClass;
	
	HashMap<Gene,List<SNP>> genetoSNPs = new 	 HashMap<Gene,List<SNP>>();
	
	
  
public String getEntry() {
		return entry;
	}



	public void setEntry(String entry) {
		this.entry = entry;
	}



	public String getId() {
		return id;
	}



	public void setId(String id) {
		this.id = id;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getPathwayClass() {
		return pathwayClass;
	}



	public void setPathwayClass(String pathwayClass) {
		this.pathwayClass = pathwayClass;
	}



	public HashMap<Gene, List<SNP>> getGenetoSNPs() {
		return genetoSNPs;
	}



	public void setGenetoSNPs(HashMap<Gene, List<SNP>> genetoSNPs) {
		this.genetoSNPs = genetoSNPs;
	}



public KeggPathway(String name) {
		super();
		this.name = name;
	}
	
	
}
